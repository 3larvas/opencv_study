class rawProtocal(Protocol):
    # 연결 시작시 발생
    def connection_made(self, transport):
        self.transport = transport
        self.running = True

    # 연결 종료시 발생
    def connection_lost(self, exc):
        self.transport = None

    #데이터가 들어오면 이곳에서 처리함.
    def data_received(self, data):
        print('recv : ', data)

    # 데이터 보낼 때 함수
    def write(self,data):
        print('send : ', data)
        self.transport.write(data)

    # 종료 체크
    def isDone(self):
        return self.running



